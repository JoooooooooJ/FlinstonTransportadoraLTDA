    private void typeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_typeActionPerformed
        if(type.getSelectedItem().toString().equals("Origem")||type.getSelectedItem().toString().equals("Destino")){
            lblPrice.setVisible(true);
            price.setVisible(true); 
        }else{
            lblPrice.setVisible(false);
            price.setVisible(false); 
        }
    }//GEN-LAST:event_typeActionPerformed

    private Address newAddress() {
        Address address = new Address();
        address.setId(new Random().nextLong());
        address.setStreet(street.getText());
        address.setNumber(Integer.parseInt(number.getText()));
        address.setCity(city.getText());
        address.setCEP(cep.getText());
        address.setUF(uf.getSelectedItem().toString());
        address.setType(type.getSelectedItem().toString());
        return address;
    }

    private boolean isEmpty() {
        return street.getText().trim().equals("")&&
                number.getText().trim().equals("")&&
                city.getText().trim().equals("")&&
                cep.getText().trim().equals("");
    }

    private void clearScreen() {
        street.setText("");
        number.setText("");
        city.setText("");
        cep.setText("");
        uf.setSelectedIndex(0);
        type.setSelectedIndex(0);
        lblDriver.setVisible(false);
        driver.setVisible(false);
    }
