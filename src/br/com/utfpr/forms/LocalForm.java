    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if(!isEmpty()){

            try {
                new AddressDao().add(newAddress());
                JOptionPane.showMessageDialog(rootPane,"Endereco inserido com sucesso");
                 if(type.getSelectedItem().toString().equals("Origem")){
                     Origin origin = new Origin(newAddress().getId(),newAddress().toString(),Double.parseDouble(price.getText()));
                     origin.add(origin);
                 }else if(type.getSelectedItem().toString().equals("Destino")){
                     Destination destination = new Destination(newAddress().getId(),newAddress().toString(),Double.parseDouble(price.getText()));
                     destination.add(destination);
                 }
            } catch (RuntimeException e) {
                JOptionPane.showMessageDialog(rootPane,"Erro ao Inserir no Banco!\n" + e);
            }
            clearScreen();
        }
        else{
            JOptionPane.showMessageDialog(rootPane,"Por favor preencha os campos obrigat√≥rios");
        }
    }//GEN-LAST:event_addActionPerformed

    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        if(!isEmpty()){
            try{
                new AddressDao().remove(newAddress());
                JOptionPane.showMessageDialog(rootPane,"Endereco inserido com sucesso");
            }catch(RuntimeException e){

                JOptionPane.showMessageDialog(rootPane,"Erro o excluir dados do banco\n" + e);
            }
            clearScreen();
        }else{
            JOptionPane.showMessageDialog(rootPane,"Para excluir dados √© preciso primeiro carreg√°-los\n"
                + "Utilize a barra de pesquisa a sua direita" );
        }
    }//GEN-LAST:event_removeActionPerformed

    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        if(!byCEP.getText().trim().equals("")){

            try{
                Address address = (Address)new AddressDao().readByCEP(byCEP.getText());
                street.setText(address.getStreet());
                number.setText(Integer.toString(address.getNumber()));
                city.setText(address.getCity());
                uf.setSelectedItem(address.getUF());
                cep.setText(address.getCEP());
                if(address.getType()!=null){
                    type.setSelectedItem(address.getType());
                    if(type.getSelectedItem().toString().equals("Origem")){                     
                        price.setText(Double.toString(new Origin().read(address.toString()).getPrice()));
                        
                    }else if(type.getSelectedItem().toString().equals("Destino")){
                        price.setText(Double.toString(new Destination().read(address.toString()).getPrice()));
                    }
                }                
                String  driverS = new DriverDao().read(Long.parseLong(byCEP.getText())).toString();
                if(!driverS.equals("0-null")){
                    driver.setVisible(true);
                    lblDriver.setVisible(true);
                    driver.setText(driverS);
                }
                    
            }catch(RuntimeException e){
                JOptionPane.showMessageDialog(rootPane,"Erro ao Consultar dados no Banco!\n" + e);
                clearScreen();
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"Digite algo na barra de pesquisa");
        }
        byCEP.setText("");
    }//GEN-LAST:event_findActionPerformed

